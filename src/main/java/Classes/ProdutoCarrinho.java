package Classes;

public class ProdutoCarrinho {
    public int id;
    public String nome;
    public String nomeLoja;
    public float preco;
    
    public ProdutoCarrinho(int id, String nome, String nomeLoja, float preco) {
	this.id = id;
	this.nome = nome;
	this.nomeLoja = nomeLoja;
	this.preco = preco;
    }
}
